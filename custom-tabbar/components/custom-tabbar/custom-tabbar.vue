<template>
	<view 
		class="custom-tabbar"
		:class="[direction]"
		:style="tabbarStyle"
	>
		<!-- Ëá™ÂÆö‰πâTabBarÊ†áËØÜ -->
		<view class="custom-tabbar-indicator" title="ËøôÊòØËá™ÂÆö‰πâTabBar">
			<text class="indicator-text">üéØ</text>
		</view>
		<view 
			v-for="(item, index) in tabList"
			:key="index"
			class="tabbar-item"
			:class="{ active: selected === index }"
			@tap="onTabItemTap(index)"
			@touchstart="onTouchStart($event, index)"
		>
			<!-- Ê∞¥Ê≥¢Á∫πÂÆπÂô® -->
			<view class="ripple-container" :ref="`ripple-${index}`"></view>
			<!-- ÂõæÊ†á -->
			<view class="tabbar-icon" v-if="showIcon">
				<image 
					:src="selected === index ? item.selectedIconPath : item.iconPath"
					class="icon-image"
					mode="aspectFit"
				/>
				<!-- Á∫¢ÁÇπ -->
				<view 
					v-if="item.showRedDot && !item.badge"
					class="red-dot"
				></view>
				<!-- ËßíÊ†á -->
				<view 
					v-if="item.badge && !item.showRedDot"
					class="badge"
				>
					<text class="badge-text">{{ item.badge }}</text>
				</view>
			</view>
			
			<!-- ÊñáÂ≠ó -->
			<text 
				class="tabbar-text"
				:style="{ color: selected === index ? props.selectedColor : props.color }"
			>
				{{ item.text }}
			</text>
		</view>
	</view>
</template>

<script setup>
import { ref, computed, onMounted } from 'vue'

// PropsÂÆö‰πâ
const props = defineProps({
	// tabbarÈ°πÊï∞ÊçÆ
	tabs: {
		type: Array,
		default: () => [],
		required: true
	},
	// ÂΩìÂâçÈÄâ‰∏≠È°πÁ¥¢Âºï
	selected: {
		type: Number,
		default: 0
	},
	// ÊòØÂê¶ÊòæÁ§∫ÂõæÊ†á
	showIcon: {
		type: Boolean,
		default: true
	},
	// ÊéíÂàóÊñπÂêë
	direction: {
		type: String,
		default: 'horizontal', // horizontal | vertical
		validator: (value) => ['horizontal', 'vertical'].includes(value)
	},
	// ÈªòËÆ§È¢úËâ≤
	color: {
		type: String,
		default: '#999999'
	},
	// ÈÄâ‰∏≠È¢úËâ≤
	selectedColor: {
		type: String,
		default: '#007aff'
	},
	// Ëá™ÂÆö‰πâÊ†∑Âºè
	customStyle: {
		type: Object,
		default: () => ({})
	}
})

// EmitsÂÆö‰πâ
const emit = defineEmits(['tabChange', 'onTabItemTap'])

// ÂìçÂ∫îÂºèÊï∞ÊçÆ
const tabList = ref([])

// ËÆ°ÁÆóÂ±ûÊÄß
const tabbarStyle = computed(() => {
	return {
		backgroundColor: '#ffffff',
		...props.customStyle
	}
})

// ÂàùÂßãÂåñtabBarÊï∞ÊçÆ
const initTabBarData = () => {
	if (props.tabs && props.tabs.length > 0) {
		tabList.value = props.tabs.map(item => ({
			...item,
			showRedDot: false,
			badge: ''
		}))
		console.log('‚úÖ Ëá™ÂÆö‰πâTabBarÂàùÂßãÂåñÂÆåÊàê:', tabList.value)
	} else {
		console.warn('‚ö†Ô∏è Ëá™ÂÆö‰πâTabBarÈúÄË¶Å‰º†ÂÖ•tabsÂèÇÊï∞')
	}
}

// Ê∞¥Ê≥¢Á∫πÊïàÊûú
const createRipple = (event, index) => {
	// Ëé∑ÂèñÁÇπÂáª‰ΩçÁΩÆÂíåÂÖÉÁ¥†‰ø°ÊÅØ
	const target = event.currentTarget
	const rect = target.getBoundingClientRect()
	const size = Math.max(rect.width, rect.height)
	const x = event.clientX - rect.left - size / 2
	const y = event.clientY - rect.top - size / 2
	
	// ÂàõÂª∫Ê∞¥Ê≥¢Á∫πÂÖÉÁ¥†
	const ripple = document.createElement('view')
	ripple.classList.add('ripple')
	ripple.style.width = ripple.style.height = size + 'px'
	ripple.style.left = x + 'px'
	ripple.style.top = y + 'px'
	
	// ÊâæÂà∞Ê∞¥Ê≥¢Á∫πÂÆπÂô®Âπ∂Ê∑ªÂä†ÂÖÉÁ¥†
	const container = target.querySelector('.ripple-container')
	if (container) {
		container.appendChild(ripple)
		
		// Ëß¶ÂèëÂä®Áîª
		requestAnimationFrame(() => {
			ripple.classList.add('ripple-animate')
		})
		
		// Âä®ÁîªÁªìÊùüÂêéÊ∏ÖÁêÜ
		setTimeout(() => {
			if (ripple && ripple.parentNode) {
				ripple.parentNode.removeChild(ripple)
			}
		}, 600)
	}
}

// Â∞èÁ®ãÂ∫èÁ´ØÊ∞¥Ê≥¢Á∫πÊïàÊûúÔºà‰ΩøÁî®CSSÂä®ÁîªÔºâ
const createMiniProgramRipple = (index) => {
	const rippleContainers = document.querySelectorAll('.ripple-container')
	const container = rippleContainers[index]
	
	if (container) {
		// ÂàõÂª∫Ê∞¥Ê≥¢Á∫πÂÖÉÁ¥†
		const ripple = document.createElement('view')
		ripple.classList.add('ripple', 'ripple-center')
		container.appendChild(ripple)
		
		// Ëß¶ÂèëÂä®Áîª
		setTimeout(() => {
			ripple.classList.add('ripple-animate')
		}, 10)
		
		// Âä®ÁîªÁªìÊùüÂêéÊ∏ÖÁêÜ
		setTimeout(() => {
			if (ripple && ripple.parentNode) {
				ripple.parentNode.removeChild(ripple)
			}
		}, 600)
	}
}

// Ëß¶Êë∏ÂºÄÂßã‰∫ã‰ª∂ÔºàÁî®‰∫éÊ∞¥Ê≥¢Á∫πÊïàÊûúÔºâ
const onTouchStart = (event, index) => {
	// #ifdef H5
	if (event.clientX !== undefined) {
		createRipple(event, index)
	} else {
		createMiniProgramRipple(index)
	}
	// #endif
	
	// #ifdef MP
	createMiniProgramRipple(index)
	// #endif
}

// TabÁÇπÂáª‰∫ã‰ª∂
const onTabItemTap = (index) => {
	const item = tabList.value[index]
	if (!item) return
	
	// Â¶ÇÊûúÁÇπÂáªÁöÑÊòØÂΩìÂâçÈÄâ‰∏≠È°πÔºåÊ∑ªÂä†ÂºπË∑≥Âä®Áîª
	if (props.selected === index) {
		triggerBounceAnimation(index)
		return
	}
	
	console.log(`üîÑ Ëá™ÂÆö‰πâTabBarÁÇπÂáª: ${item.text} (Á¥¢Âºï: ${index}) üåä`)
	
	// Ëß¶ÂèëÂàáÊç¢Âä®Áîª
	triggerSwitchAnimation(props.selected, index)
	
	// Ëß¶Âèë‰∫ã‰ª∂
	emit('tabChange', index, item)
	emit('onTabItemTap', {
		index,
		pagePath: item.pagePath,
		text: item.text
	})
	
	// ÊâßË°åÈ°µÈù¢Ë∑≥ËΩ¨
	uni.switchTab({
		url: `/${item.pagePath}`,
		fail: (err) => {
			console.error('È°µÈù¢Ë∑≥ËΩ¨Â§±Ë¥•:', err)
		}
	})
}

// Ëß¶ÂèëÂºπË∑≥Âä®ÁîªÔºàÁÇπÂáªÂΩìÂâçÈÄâ‰∏≠È°πÊó∂Ôºâ
const triggerBounceAnimation = (index) => {
	console.log(`üéØ ÂΩìÂâçÈ°πÂºπË∑≥Âä®Áîª: ${index}`)
	// ËøôÈáåÂèØ‰ª•Ê∑ªÂä†È¢ùÂ§ñÁöÑÂºπË∑≥Âä®ÁîªÈÄªËæë
}

// Ëß¶ÂèëÂàáÊç¢Âä®Áîª
const triggerSwitchAnimation = (fromIndex, toIndex) => {
	console.log(`üîÑ ÂàáÊç¢Âä®Áîª: ${fromIndex} -> ${toIndex}`)
	// ÂèØ‰ª•Âú®ËøôÈáåÊ∑ªÂä†Êõ¥Â§çÊùÇÁöÑÂàáÊç¢Âä®ÁîªÈÄªËæë
}

// ËÆæÁΩÆtabBarËßíÊ†á
const setTabBarBadge = (index, text) => {
	if (tabList.value[index]) {
		tabList.value[index].badge = text
		tabList.value[index].showRedDot = false
	}
}

// ÁßªÈô§tabBarËßíÊ†á
const removeTabBarBadge = (index) => {
	if (tabList.value[index]) {
		tabList.value[index].badge = ''
	}
}

// ÊòæÁ§∫tabBarÁ∫¢ÁÇπ
const showTabBarRedDot = (index) => {
	if (tabList.value[index]) {
		tabList.value[index].showRedDot = true
		tabList.value[index].badge = ''
	}
}

// ÈöêËóètabBarÁ∫¢ÁÇπ
const hideTabBarRedDot = (index) => {
	if (tabList.value[index]) {
		tabList.value[index].showRedDot = false
	}
}

// Êö¥Èú≤ÊñπÊ≥ïÁªôÂ§ñÈÉ®Ë∞ÉÁî®
defineExpose({
	setTabBarBadge,
	removeTabBarBadge,
	showTabBarRedDot,
	hideTabBarRedDot,
	tabList
})

// ÁªÑ‰ª∂ÊåÇËΩΩ
onMounted(() => {
	initTabBarData()
})
</script>

